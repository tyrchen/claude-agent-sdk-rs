def add(a, b):
    """Add two numbers and return the result.

    Args:
        a: First number
        b: Second number

    Returns:
        The sum of a and b
    """
    return a + b


def subtract(a, b):
    """Subtract b from a and return the result.

    Args:
        a: First number
        b: Second number

    Returns:
        The difference of a and b
    """
    return a - b


def multiply(a, b):
    """Multiply two numbers and return the result.

    Args:
        a: First number
        b: Second number

    Returns:
        The product of a and b
    """
    return a * b
